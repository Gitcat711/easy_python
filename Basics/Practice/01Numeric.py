#Arithmetic Operators:|
#=====================|
# 1.Addition: x + y
print(4 + 6)
summ =  6 + 90
print(summ)
print("-" * 20)
# 2.Substraction: x - y
print(7 - 9 ) #
sub = 94 - 28
print(sub)
print("-" * 20)

# 3.Multiplication: x * y
print(12 * 76)
mult = 34 * 67
print(mult)
print("-" * 20)

# 4.Division: x/y (gives us float, doesn't drop the number after decimal) 
print(67/7)
div = 45 / 5
div2 = 29 / 7
print(div)
print(div2)
print("-" * 20)

# 5. Floor Division: (x // y) (drops the number after decimal by default)
print(256 // 6)
fdiv = (56 // 9)
print(fdiv)
print("-" * 20)

# 6. Exponent: x ** y
print(7**3)
expo = 2 ** 8
print(expo)
print("-" * 20)

# 7. Modulus: x % y, this gives us a remainder
print(23 % 5)
modu = 45 % 8
print(modu)	
print("-" * 20)

