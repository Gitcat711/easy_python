# we will find out whether the number is positive or negative.
a = int(input("Enter the number="))
if a<0:#here the number will be checked whether the it is less than zero.
    print("The number is negative")#if the above conditon satisfies it will the output.
elif  a>0:# elif is used when there is more than two condition.
    print("The number is positive")
else:#if above conditions are not satisfied then else function will break that and print the output zero.
    print("The number is zero")
