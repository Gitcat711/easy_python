# program to find the sum of all numbers present in list(you can edit the values of list)
a = [5,6,7,8,21,9,11,41] # list is always written in square bracket.
sum = 0 # variable to  store the sum.
for i in a:# here the loop is applied to repeat over list.
    sum =sum+i
print("The sum is",sum)#this will print the sum of numbers present in the list
